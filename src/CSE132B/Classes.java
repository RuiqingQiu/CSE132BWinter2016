package CSE132B;

public class Classes {
	public String SectionID;
	public String Title;
	public String Quarter;
	public String year;
	public int MaxEnrollment;
	
	public Classes(String SectionID, String Title, String Quarter, String year, int MaxEnrollment){
		this.SectionID = SectionID;
		this.Title = Title;
		this.Quarter = Quarter;
		this.year = year;
		this.MaxEnrollment = MaxEnrollment;
	}
}
